      Coucou, je suis Muriel, créatrice située à Montastruc-La-Conseillère.<br> Après avoir testé le pastel et la peinture à relief sur tableau, j’ai découvert la résine sur toile, et ça a été le coup de cœur. J’adore cette matière qui permet autant de champs des possibles!
      Étant une fan inconditionnelle de bijoux, j’ai tout naturellement appliqué la technique de la résine dans la création de bijoux.<br>
      La résine est associée au verre, à des métaux précieux, des pierres fines... qui permet des inclusions de toutes sortes: feuilles d’or ou d’argent, peinture à effet, papier de soie, tissus, pigments colorés, petits objets, etc.
      Résistante, lumineuse, flamboyante, aux couleurs riches et profondes, cette matière permet de réaliser de grosses pièces tout en gardant une légèreté.
      <br>Les bijoux des Envies de Mû sont des modèles uniques en acier inoxydable, en laiton sans nickel, sans plomb et sans cadium. Certains modèles sont en plaqué or ou plaqué argent. 
      <br>Il est possible de personnaliser votre bijou aussi bien dans le choix d’inclusion dans la résine (avec une de vos photos, une couleur, tissus, objets...),  que dans le montage (choix de la matière du support, pampilles, connecteurs...).
      Les Envies de Mû, c’est vous faire plaisir tout en réalisant vos envies et mes envies...
