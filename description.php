      Derrière Les Envies de Mû, il y a … Muriel , créatrice toulousaine.<br>

Après avoir travaillée 17 ans dans le handicap, mais également après un mariage, 2 enfants, une construction de maison (nous l’avons construite nous même), et j’en passe… Je me suis rendu compte qu’il me manquait quelque chose… Mais quoi ? J‘avais tout ce qu’il me fallait et tout se passait bien mais je n’étais pas vraiment épanouie…
Le déclic, je l’ai eu lorsque mon mari m’a offert une formation « découverte de la résine sur tableau ». 
EUREKA !!!! Voilà ce qu’il me manquait pour être épanouie… La création !!!<br>

Et me voilà partie dans mes heures perdues à faire des tableaux en résine et approfondir mes connaissances sur ce qu’on peut réaliser à partir de la résine… Dont les bijoux !
Les bijoux ? Moi, qui adore les bijoux fantaisies ! Qui adore changer de bijoux en fonction de mes habits et de mes humeurs !! Que demander mieux ?
Je trouve qu’un bijou est l’indispensable à une tenue. C’est le petit truc en plus, la toute petite cerise sur le gâteau, mais… Qui change tout ! 
C’est donc tout naturellement que je me lance dans la création de bijoux pour mon plaisir… Puis pour mes amies… voisines…  collègues… qui sont tentées en me voyant tous les jours avec de nouveaux bijoux… 
Ces mêmes personnes m’ont ensuite commandé des bijoux pour offrir des cadeaux, puis ma famille se rajoutent à la liste des personnes qui apprécient mes bijoux et m’en commandent…
Je me suis rendue compte que plus je créais, plus je m’épanouissais. Et plus je m’épanouissais, plus j‘avais des idées et de l’inspiration pour faire toujours plus, toujours mieux, plus original…
Maintenant, j’écoute mes envies, je prends soin de moi et j’essaie de penser un peu plus à moi et un peu moins aux autres… C’est donc assez naturellement que le nom « Les Envies de Mû » m’est apparu !!!

« J’aime me dépasser et toujours trouver de nouvelles idées de plus en plus originales… Agrandir le champ des possibles… »


                  
